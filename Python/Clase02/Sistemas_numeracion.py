# Sistema decimal
a = 10
print(f'a decimal: {a}')

# Sistema binario
a = 0b1010
print(f'a binario: {a}')

# Sistema octal
a = 0o12
print(f'a octal: {a}')

# Sistema hexadecimal
a = 0xA
print(f'a hexadecimal: {a}')

# Base decimal
a = int('23', 10)
print(f'a = decimal: {a}')

# Base binario
a = int('1010', 2)
print(f'a = binario: {a}')

# Base octal
a = int('12', 8)
print(f'a = base octal: {a}')

# Base Hexadecimal
a = int('17', 6)
print(f'a = hexadecimal: {a}')

# Base 5 (de 0 a 4)
a = int('344', 5)
print(f'a = base 5: {a}')




















